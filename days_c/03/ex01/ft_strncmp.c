int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (i < n)
	{
		if (s1[i] != s2[i] || !s1[i + 1] || !s2[i + 1])
		{
			return (s1[i] - s2[i]);
		}
		i += 1;
	}
	return (0);
}
