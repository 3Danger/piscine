#include <unistd.h>

void	ft_putchar9(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
	}
	else if (nb < 0)
	{
		ft_putchar9('-');
		ft_putnbr(nb * -1);
	}
	ft_putchar9('0' + nb % 10);
}
